export const getCarrierById = (id: string): InsuranceCarrier | undefined => {
  return insuranceCarriers.find((carrier) => carrier.id === id);
};

export const getInsuranceByState = (
  abbreviation: string
): InsuranceByState | undefined => {
  return insuranceByState.find((state) => state.abbreviation === abbreviation);
};
